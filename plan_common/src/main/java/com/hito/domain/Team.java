package com.hito.domain;

public class Team {
	private int id;
	private String create_p;
	private String team_title;

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getCreate_p() {
		return create_p;
	}

	public void setCreate_p(String create_p) {
		this.create_p = create_p;
	}

	public String getTeam_title() {
		return team_title;
	}

	public void setTeam_title(String team_title) {
		this.team_title = team_title;
	}

}
